class Calculations:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def add(self):
        return self.a + self.b
    
    def subtract(self):
        return self.a - self.b
    
    def product(self):
        return self.a * self.b
    
    def quotioent(self):
        return self.a / self.b
    
    